#include "../../../include/cub3d.h"

int		ft_space(char **newstr, int i, int j, char c)
{
	if (newstr[i][j] == c && (
		newstr[i][j + 1] == ' ' ||
		newstr[i][j - 1] == ' ' ||
		newstr[i + 1][j] == ' ' ||
		newstr[i - 1][j] == ' '))
		return (1);
	return (0);
}
